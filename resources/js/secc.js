$('#logout').click(function(){


        $.ajax({
        url: "http://localhost/SFL-Admin/index.php/api/users/logout",
        data: {
            session : sessionStorage.getItem("session-sfl"),
            id : sessionStorage.getItem("id-sfl")
        },
        type: "POST",
        dataType: "json",
        success: function(source) {
            if (source.success === true) {

                        sessionStorage.setItem("id-sfl", "");
                        sessionStorage.setItem("session-sfl", "");
           


                location.href='http://localhost/sfl-aspirantes/perfil.php';
            } else {
                console.log(source.message);
            }
        },
        error: function(dato) {
            console.log(JSON.stringify(dato));
            console.log("ERROR");
        }
    });


    });